def gauss_elimination(X):
    A = X.copy()
    n, m = A.shape
    for i in range(0, n):
        # Search for maximum 
        maxEl = abs(A[i][i])
        maxRow = i
        for k in range(i+1, n):
            if abs(A[k][i]) > maxEl:
                maxEl = abs(A[k][i])
                maxRow = k

        if i != maxRow:
            tmp = A[i, :].copy()
            A[i, :] = A[maxRow, :]
            A[maxRow, :] = tmp


        # 対角成分以下をゼロ
        for k in range(i+1, n):
            c = -A[k][i]/A[i][i]
            for j in range(i, n+1):
                if i == j:
                    A[k][j] = 0
                else:
                    A[k][j] += c * A[i][j]

    # Solve equation Ax=b
    x = [0 for i in range(n)]
    for i in range(n-1, -1, -1):
        x[i] = A[i][n]/A[i][i]
        for k in range(i-1, -1, -1):
            A[k][n] -= A[k][i] * x[i]
    return x